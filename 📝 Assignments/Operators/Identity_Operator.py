x = [1, 2, 3]
y = x
z = [1, 2, 3]

# is (identity Operator)
# is not (Not identity operator)
# == (value compare)

print(x is y) # Output: True
print(x is z) # Output: False
print(x == z) # Output: True
print(x is not z) # Output: True

