x = 5
y = 3

print(x == y) # False (equal to)
print(x != y) # True (not equal to)
print(x > y) #  True (greater than)
print(x < y) # False (less than)
print(x >= y) # True (greater than OR equal to)
print(x <= y) # False (less than OR equal to)

