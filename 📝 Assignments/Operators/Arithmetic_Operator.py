a = 10
b = 3

# Addition
print(a + b) # Output: 13

# Subtraction
print(a - b) # Output: 7

# Multiplication
print(a * b) # Output: 30

# Division (Give float result)
print(a / b) # Output: 3.333

# Floor Division (only integer part)
print(a // b) # Output: 3

# Modulus (Remainder)
print(a % b) # Output: 1

# Exponentiataion (Power)
print(a ** b) # Output: 1000 (10^3)

