x = 5
y = 10

# AND Operator
print((x > 2) and (y < 15)) # Output: True
print((x > 2) and (y > 15)) # Output: False

# OR Operator
print((x > 2) or (y > 15)) # Output: True
print((x < 2) or (y > 15)) # Output: False

# NOT Operator
print(not(x > 2)) # Output: False
print(not(y > 15)) # Output: True

