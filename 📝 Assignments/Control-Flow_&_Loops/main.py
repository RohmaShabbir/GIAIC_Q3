# If-Else Statement
age = 18

if age >= 18:
    print("You are eligible to vote!")
else:
    print("Yor are not eligible to vote!")

# Elif (Else If)
marks = 75

if marks >= 90:
    print("Grade: A")
elif marks >= 80:
    print("Grade: B")
elif marks >= 70:
    print("Grade: C")
else:
    print("Fail")

